<p>ここにあるすべてのプラクティスは「<a href="https://www.mobiusloop.com/" target="_blank" rel="noopener noreferrer">Outcome Delivery<ExternalLinkIcon/></a>フレームワーク」と呼ばれる考え方を基にして、分類分けをしています。</p>
<h2 id="outcome-deliveryフレームワークとは" tabindex="-1"><a class="header-anchor" href="#outcome-deliveryフレームワークとは" aria-hidden="true">#</a> Outcome Deliveryフレームワークとは</h2>
<p><img src="/Mobius.svg" alt="Image of Mobius Loop"></p>
<blockquote>
<p>参照元：<a href="https://openpracticelibrary.com/" target="_blank" rel="noopener noreferrer">The Open Practice Library<ExternalLinkIcon/></a></p>
