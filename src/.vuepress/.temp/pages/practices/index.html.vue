<p>上の図で表されるように、<a href="https://www.mobiusloop.com/" target="_blank" rel="noopener noreferrer">Outcome Delivery<ExternalLinkIcon/></a>フレームワークは三つのフェーズに別れています。</p>
<ul>
<li>Discoveryループ : 成果を構想するフェーズ</li>
<li>Options Pivot  : 方法を確認・決定するフェーズ</li>
<li>Deliveryループ  : テストのためにアイディアを実装し、学習するフェーズ</li>
</ul>
<p>これらに加え、フレームワークの基盤となる「Foundation」があり、</p>
<p>文化と協同、エンジニアリングについてのプラクティスで上記三つのフェーズをサポートしています。</p>
<h2 id="プラクティス一覧" tabindex="-1"><a class="header-anchor" href="#プラクティス一覧" aria-hidden="true">#</a> プラクティス一覧</h2>
<h3 id="discoveryループ" tabindex="-1"><a class="header-anchor" href="#discoveryループ" aria-hidden="true">#</a> Discoveryループ</h3>
<p>「Discovery」は「発見」という意味です。</p>
<p>このフェーズでは課題がどこにあるのか、誰の課題なのか、なぜ必要なのかなどを発見していきます。</p>
<p>ループとなっているのは、何度も何度も繰り返し行うため、ループとなっています。</p>
<ul>
<li><a href="/practices/empathy-mapping">共感マッピング/empathy mapping</a></li>
<li><a href="/practices/value-stream-process-mapping">バリューストリーム・マッピングおよびプロセスマッピング/Value Stream &amp; Process Mapping</a></li>
<li><a href="/practices/impact-mapping">インパクトマッピング/impact mapping</a></li>
<li>イベントストーミング/event storming</li>
<li><a href="/practices/user-story-mapping">ユーザーストーリーマッピング/user story mapping</a></li>
</ul>
<h3 id="options-pivot" tabindex="-1"><a class="header-anchor" href="#options-pivot" aria-hidden="true">#</a> Options Pivot</h3>
<p>「Options」は「選択肢」という意味で、</p>
<p>「Pivot」は「方向転換」という意味です。</p>
<p>成果をどうやって顧客に届けるのかを確認し、決定するのが「Options Pivot」です。</p>
<p>「選択肢」を改めて調べて、</p>
<p>必要があれば現状から方向転換することを決定するフェーズです。</p>
<ul>
<li><a href="/practices/backlog-refinement">バックログ・リファインメント/backlog refinement</a></li>
</ul>
<h3 id="deliveryループ" tabindex="-1"><a class="header-anchor" href="#deliveryループ" aria-hidden="true">#</a> Deliveryループ</h3>
<p>「Delivery」は「届ける」という意味です。</p>
<p>「Options Pivot」で決定した選択肢を顧客に届けるために作成し、</p>
<p>顧客やステークホルダーにフィードバックをもらい、学習していきます。</p>
<ul>
<li><a href="/practices/iteration-planning">スプリント（イテレーション）計画/sprint planning</a></li>
<li><a href="/practices/daily-standup">毎日スタンドアップ・ミーティング/daily standup</a></li>
<li><a href="/practices/retrospectives">ふりかえり/retrospectives</a></li>
</ul>
<h3 id="foundation" tabindex="-1"><a class="header-anchor" href="#foundation" aria-hidden="true">#</a> Foundation</h3>
<p>「Foundation」は、「基盤」という意味です。</p>
<p>上記三つのフェーズを支えるために</p>
<p>チーム文化を作る</p>
<p>協働できる環境を作る</p>
<p>技術的に有効なプラクティスを行う</p>
<p>といったような</p>
<p>「Discovery」や「Delivery」のループを円滑に行うための支えになるようなプラクティスが用意されています。</p>
<p>「Foundation」のプラクティスにより、</p>
<p>チームは持続可能に継続的なDeliveryを行うことができるようになります。</p>
