<h2 id="プラクティスとは" tabindex="-1"><a class="header-anchor" href="#プラクティスとは" aria-hidden="true">#</a> プラクティスとは</h2>
<p>日本語ではあまり馴染みのない「プラクティス」という言葉ですが、</p>
<p>「習慣」ととらえていただくのが一番わかりやすいかと思います。</p>
<p>課題を解決するために最適化された組織に必要な様々な「習慣」を「プラクティス」としています。</p>
<h2 id="プラクティス一覧とは" tabindex="-1"><a class="header-anchor" href="#プラクティス一覧とは" aria-hidden="true">#</a> プラクティス一覧とは</h2>
