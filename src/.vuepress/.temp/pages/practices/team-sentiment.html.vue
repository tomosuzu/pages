<h3 id="応用" tabindex="-1"><a class="header-anchor" href="#応用" aria-hidden="true">#</a> 応用</h3>
<p>応用ですが、いくつかのトピックを元に感情を評価する下記のヘルスチェック手法も有効です。（英語の記事）</p>
<ul>
<li><a href="https://labs.spotify.com/2014/09/16/squad-health-check-model/" target="_blank" rel="noopener noreferrer">Squad Health Check model – visualizing what to improve<ExternalLinkIcon/></a></li>
</ul>
<p>簡単に説明しますと</p>
<p>チーム感情をいくつかのトピックに沿った形で評価します。</p>
<p>例えば、</p>
<ul>
<li>開発スピードは速いですか？</li>
<li>リリースに障害はないですか？</li>
<li>学びはありますか？</li>
</ul>
<p>などといったものです。</p>
<p>最初、チーム感情の評価を行うと何を書いていいかわからないなど</p>
<p>慣れていない場合にも取り入れてみても面白いと思います。</p>
<p>また、毎回同じトピックで評価するので</p>
<p>時系列でチームの状態を追うことができます。</p>
<h2 id="注意点" tabindex="-1"><a class="header-anchor" href="#注意点" aria-hidden="true">#</a> 注意点</h2>
<h3 id="空気を読みすぎない" tabindex="-1"><a class="header-anchor" href="#空気を読みすぎない" aria-hidden="true">#</a> 空気を読みすぎない</h3>
<p>悪い感情を出しづらい雰囲気にならないように気をつけましょう。</p>
<p>他のメンバーが良い雰囲気だと言っているからといって</p>
<p>良い雰囲気を壊さないようにしようと</p>
<p>課題と思っていることを言わないでいるのは避けましょう。</p>
<p>いわゆる、空気を読みすぎないように気をつけましょう。</p>
