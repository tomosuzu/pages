<template><div><h1 id="チーム感情の評価" tabindex="-1"><a class="header-anchor" href="#チーム感情の評価" aria-hidden="true">#</a> チーム感情の評価</h1>
<p>〜チームの健康状態を知り、様々な問題に事前に対処〜</p>
<h2 id="チーム感情の評価とは" tabindex="-1"><a class="header-anchor" href="#チーム感情の評価とは" aria-hidden="true">#</a> チーム感情の評価とは</h2>
<p>「チーム感情の評価」は、チームの感情を定期的に評価し、</p>
<p>感情が変わったときにチームメンバーがオープンにまたは匿名で迅速にフィードバックするための手法です。</p>
<h2 id="なぜチーム感情の評価をするのか" tabindex="-1"><a class="header-anchor" href="#なぜチーム感情の評価をするのか" aria-hidden="true">#</a> なぜチーム感情の評価をするのか</h2>
<p>チーム感情の評価を行うことで、問題を早期に特定、周知し、対処することができます。</p>
<p>チームが一人または複数のチームメンバーが自分の仕事で困っているところを議論することができ、</p>
<p>チーム全体の健康状態と感情を評価するための情報源になります。</p>
<p>チーム全体の健康状態を知ることでわかるのは</p>
<p>メンバーの不満やストレスが溜まっていて退職してしまうことを防げるといったことだけではありません。</p>
<p>チームの健康状態が悪くなるということは</p>
<p>作成物の品質や開発スピード、リリース頻度などにも影響がでている可能性が高いです。</p>
<p>チームの健康状態の低下を放って置かず、</p>
<p>少しでも悪化したらどうやって改善するかを考えることで</p>
<p>未然に大きなトラブルを防ぐことにつながっていきます。</p>
<h2 id="どうやっておこなうのか" tabindex="-1"><a class="header-anchor" href="#どうやっておこなうのか" aria-hidden="true">#</a> どうやっておこなうのか</h2>
<p>定期的にチームメンバー自身の気分を赤や緑の付箋紙で評価します。</p>
<p>例えば、</p>
<p>赤：非常に悪い</p>
<p>黄：悪くなるかも</p>
<p>緑：良好</p>
<p>などのように決めて、各自、みんなが見えるところに貼ります。</p>
<p>もし、話し合う必要がありそうであれば、その機会を設けましょう。</p>
<p>この場合、名前を入れるか入れないかは自由とした方が</p>
<p>率直な意見をあげやすいと思います。</p>
