<p>〜作るものへの共通理解を生み出す〜</p>
<h2 id="ユーザーストーリーマッピングとは" tabindex="-1"><a class="header-anchor" href="#ユーザーストーリーマッピングとは" aria-hidden="true">#</a> ユーザーストーリーマッピングとは</h2>
<p>ユーザーストーリーマッピングとは、</p>
<p>大きなストーリーを話しながら、小さく分割していき、</p>
<p>関係者全員の共通理解を生み出すことです。</p>
<h2 id="なぜユーザーストーリーマッピングをするのか" tabindex="-1"><a class="header-anchor" href="#なぜユーザーストーリーマッピングをするのか" aria-hidden="true">#</a> なぜユーザーストーリーマッピングをするのか</h2>
<p>関係者全員の共通理解を得るために行います。</p>
<p>話しながらストーリーを作成するので、</p>
<p>ストーリーに書かれていない、前提や背景などの情報が思い出すことができます。</p>
<p>また、優先度の切り分けがしやすく、</p>
<p>アウトプットを最小限に抑え、最大限の成果（アウトカム）とインパクトを獲得することができます。</p>
<h2 id="どうやっておこなうのか" tabindex="-1"><a class="header-anchor" href="#どうやっておこなうのか" aria-hidden="true">#</a> どうやっておこなうのか</h2>
