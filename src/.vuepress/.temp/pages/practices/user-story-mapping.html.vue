<h2 id="注意点" tabindex="-1"><a class="header-anchor" href="#注意点" aria-hidden="true">#</a> 注意点</h2>
<h3 id="認識を共有したいならドキュメントを共有すればいいのではないか" tabindex="-1"><a class="header-anchor" href="#認識を共有したいならドキュメントを共有すればいいのではないか" aria-hidden="true">#</a> 認識を共有したいならドキュメントを共有すればいいのではないか</h3>
<p>共有されたドキュメントは共通認識にはなりません。</p>
<p>同じドキュメントを読んでも、</p>
<p>人によって前提知識や経験、立場が異なるため</p>
<p>共通の理解になることは困難です。</p>
<p>私が複数チームにまたがるような大規模開発をしていたとき、</p>
<p>ドキュメントに書かれたことを口頭で確認すると</p>
<p>認識が異なっていたことが多々ありました。</p>
<p>完璧なドキュメントを目指すよりも</p>
<p>ストーリーを語ることでの共通理解を目指した方が早いし、確実だと思います。</p>
<h3 id="誰、何、なぜ、どうやってなどを語ろう" tabindex="-1"><a class="header-anchor" href="#誰、何、なぜ、どうやってなどを語ろう" aria-hidden="true">#</a> 誰、何、なぜ、どうやってなどを語ろう</h3>
<p>ストーリーは日本語に訳すと「物語」です。</p>
<p>物語を語ってください。</p>
<p>ストーリーは使う人の目線で書きましょう。</p>
<p>下記ではなく、</p>
<p>「プロダクトオーナーがビジネス上必要だからチャットシステムを作る」</p>
<p>下記のように書きます</p>
<p>「ユーザーが商品についての詳細を気軽に尋ねられるように画面内からサポートに問い合わせできる」</p>
